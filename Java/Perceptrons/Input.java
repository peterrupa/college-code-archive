class Input {
    private double x0;
    private double x1;
    private int z;
    
    public Input(double x0, double x1, int z) {
        this.x0 = x0;
        this.x1 = x1;
        this.z = z;
    }
    
    public double getX0() {
        return this.x0;
    }
    
    public double getX1() {
        return this.x1;
    }
    
    public int getZ() {
        return this.z;
    }
}